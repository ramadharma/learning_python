choice1 = input("Do you want to go 'left' or 'right'? ").lower()
if choice1 != "left":
    print("You fall into a hole. Game Over.")
else:
    # Step 2: Choose Swim or Wait
    choice2 = input("You come to a lake. Do you 'swim' or 'wait'? ").lower()
